valor1 = int(input("digite o valor1:"))
valor2 = int(input("digite o valor2:"))
operacao = input("digite o sinal (*, /, +, -)")
if operacao == "+":
    print(valor1 + valor2)
if operacao == "-":
    print(valor1 - valor2)
if operacao == "*":
    print(valor1 * valor2)
if operacao == "/":
    print(valor1 / valor2)